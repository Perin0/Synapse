  <h1>Sobre o Synapse</h1>
  
  <p>O Synapse nasceu da iniciativa de estudantes do Centro Universitário de Faculdades Integradas de Ourinhos
    (UNIFIO) com uma missão clara: democratizar o conhecimento dentro da comunidade acadêmica. Nossa plataforma
    conecta estudantes que buscam aprendizado com colegas que já dominam determinadas áreas do conhecimento,
    criando uma rede colaborativa de ensino e aprendizagem.
  </p>

  <h2>Nossa Missão</h2>
  <p>Acreditamos que o conhecimento compartilhado tem poder transformador. Por isso, desenvolvemos uma
    plataforma que facilita o acesso à orientação acadêmica e profissional, promovendo não apenas o
    desenvolvimento individual, mas fortalecendo toda a comunidade universitária.
  </p>

  <h2>Como Funcionamos</h2>
  <p>Nossa plataforma opera de forma simples e eficiente:</p>
  <ul>
    <li>Conectamos estudantes com dúvidas a mentores experientes</li>
    <li>Oferecemos mentorias em diversas áreas do conhecimento</li>
    <li>Proporcionamos um ambiente seguro para troca de experiências</li>
    <li>Facilitamos o agendamento e realização das mentorias</li>
  </ul>

  <h2>Nossos Valores</h2>
  <p>O Synapse se fundamenta em três pilares essenciais:</p>
  <ul>
    <li><strong>Colaboração:</strong> Acreditamos no poder da troca de conhecimentos entre pares</li>
    <li><strong>Qualidade:</strong> Garantimos mentores capacitados e comprometidos com o ensino</li>
    <li><strong>Acessibilidade:</strong> Tornamos o conhecimento mais acessível a todos os estudantes</li>
  </ul>

  <h2>Impacto na Comunidade</h2>
  <p>Desde nossa fundação, temos trabalhado para criar um ecossistema educacional mais colaborativo e
    eficiente. Acreditamos que cada mentoria realizada é um passo em direção a uma comunidade acadêmica
    mais forte e preparada para os desafios do futuro.
  </p>
</section>
